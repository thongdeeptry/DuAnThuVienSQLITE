package com.example.duanmau;

public class Top {
    int maSach;
    int soLuong;

    public Top(int maSach, int soLuong) {
        this.maSach = maSach;
        this.soLuong = soLuong;
    }

    public Top() {

    }

    public int getMaSach() {
        return maSach;
    }

    public void setMaSach(int maSach) {
        this.maSach = maSach;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }
}
